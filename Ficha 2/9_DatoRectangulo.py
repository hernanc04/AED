# Datos
ancho = float(input("Ingrese el ancho del rectangulo: "))
alto = float(input("Ingrese el alto del rectangulo: "))

# Se desarrolan las operaciones
per = 2*(alto + ancho)
sup = ancho * alto

# Se muestra en pantalla

print(f'Tenemos que la superficie del triangulo es de {sup} y el perimetro es de {per}')